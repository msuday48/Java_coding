package Day1_datatypes_variable_Demo;

public class DataTypeDemo {

	public static void main(String[] args) {
		
		
		//Numeric data types
		
		int a=100 , b=200;
		System.out.println(a);
		System.out.println(b);
		System.out.println(a+b);
		
		byte by=120;
		System.out.println(by);
		
		short sh=250;
		System.out.println(sh);
		
		long l=2340984121220L; // Literal is needed at the end of value
		System.out.println(l);

		//Decemal number - float double
		
		float price = 123.99f; // Literal is needed at the end of value
		System.out.println(price);
		
		double item_price=145.3265;
		System.out.println(item_price);
		
		//character 
		char grad='A';
		System.out.println("THE GRADE OF STUDENT"+ grad);
		
		//boolean - allowed only true /false
		boolean bool=true;
		System.out.println(bool);
		
		//String 
		String name="john";
		//
		
	}
}
