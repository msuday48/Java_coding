package Day3_Operators;

public class AssiggnmentOperators {

	public static void main(String[] args) {
		
		//Assignment operators
		//ex1
		 int a=10;
		 a=a+5;
		 //or
		 a+=5;
		 System.out.println(a);
		 
		 //ex2
		 int b=10;
		 b=b-5;
		 //or
		 b-=5;
		 System.out.println(b);
		 
        //ex3  
		 int c=10;
		 c=c*2;
		 c*=2;
		 System.out.println(c);
		 
		 //ex4
		int d=10; 
		d=d%2;
		d%=2;
		System.out.println(d);
		 
	}

}
