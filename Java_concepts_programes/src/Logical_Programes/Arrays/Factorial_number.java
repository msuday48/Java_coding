package Java_concepts_programes.src.Logical_Programes.Arrays;

public class Factorial_number {

    public static void main(String[]args){

        int num=5;

        long factorial=1;

        for(int i=1;1<num;i++){
            factorial=factorial*1;
        }

        System.out.println("factoril of given number"+ factorial);
    }
}
